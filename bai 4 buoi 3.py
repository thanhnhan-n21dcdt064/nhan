myList = [1, 2, 3, 4, 5, 6, 7]#là một danh sách (list) chứa các số từ 1 đến 7.
print(myList)#in ra danh sách myList ban đầu, và kết quả sẽ là [1, 2, 3, 4, 5, 6, 7].

# Chèn giá trị 11 vào vị trí đầu tiên của danh sách
myList.insert(0, 11)

print(myList)#in ra danh sách myList sau khi đã chèn giá trị 11 vào vị trí đầu tiên, và kết quả sẽ là [11, 1, 2, 3, 4, 5, 6, 7].
