myList = [1,2,3,4,5,6,7]#là một danh sách (list) chứa các số từ 1 đến 7.
print(myList)#in ra dòng list à kết quả sẽ là [1, 2, 3, 4, 5, 6, 7].
myList.insert(4,15)#chèn giá trị 15 vào vị trí thứ 4 của danh sách (myList). 4 là chỉ số, nơi giá trị 15 sẽ được chèn vào.
myList.append(55)#thêm giá trị 55 vào cuối danh sách (myList)
print(myList)#in ra danh sách myList sau khi đã thực hiện chèn và thêm giá trị mới, và kết quả sẽ là [1, 2, 3, 4, 15, 5, 6, 7, 55]
