myList = [1,2,3,4,5,6,7]# myList là một danh sách (list) chứa các số từ 1 đến 7.
print(myList)#in ra danh sách myList ban đầu, và kết quả sẽ là [1, 2, 3, 4, 5, 6, 7]
myList[2] = 33#gán giá trị 33 cho phần tử thứ 3 của danh sách (myList), vì chỉ số bắt đầu từ 0, nên đây là phần tử thứ 3.
myList[4] = 55#gán giá trị 55 cho phần tử thứ 5 của danh sách (myList).

print(myList)#in ra danh sách myList sau khi đã thay đổi, và kết quả sẽ là [1, 2, 33, 4, 55, 6, 7].